package fundamentos;

public class OperadoresAtribuicao {

	public static void main(String[] args) {
		int a = 7;
		int b = 3;
		
		// Somar b
		b += a;
		System.out.println(b);
		
		// Multiplicar b
		b *= 4;
		System.out.println(b);
		
		// Dividir b
		b /= 2;
		System.out.println(b);
		
		// Subtrair b
		b -= 10;
		System.out.println(b);
		
		// Modulo b -> resto da divis√£o
		b %= 4;
		System.out.println(b);
		
	}
}
