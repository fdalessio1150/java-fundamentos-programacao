package api;

public class ExplorandoMath {

	public static void main(String[] args) {
		
		System.out.println(Math.PI);
		System.out.println(Math.pow(2,  3));
		System.out.println(Math.abs(-10.535));
		System.out.println(Math.min(70,  6));
		System.out.println(Math.max(700, 4));
		System.out.println(Math.ceil(6.7));
		System.out.println(Math.floor(6.5));
		System.out.println(Math.random()*100);
		
		
	}

}
